# Building class

class Building:

	def __init__(self, real_estate_id, total_units, recycle_units, apt_sc_sqft,
				heated_area):
		self.__real_estate_id = real_estate_id
		self.__total_units = total_units
		self.__recycle_units = recycle_units
		self.__apt_sc_sqft = apt_sc_sqft
		self.__heated_area = heated_area